// Helper function to generate CSS custom properties
export function generateThemeCSS(theme: Theme): string {
  const cssVars: string[] = [];
  
  // Generate color variables
  Object.entries(theme.colors).forEach(([colorName, colorShades]) => {
    Object.entries(colorShades).forEach(([shade, value]) => {
      cssVars.push(`--color-${colorName}-${shade}: ${value};`);
    });
  });

  // Generate other theme variables
  cssVars.push(`--font-primary: ${theme.fonts.primary};`);
  cssVars.push(`--font-secondary: ${theme.fonts.secondary};`);
  
  Object.entries(theme.borderRadius).forEach(([size, value]) => {
    cssVars.push(`--radius-${size}: ${value};`);
  });
  
  Object.entries(theme.shadows).forEach(([size, value]) => {
    cssVars.push(`--shadow-${size}: ${value};`);
  });

  return `:root {\n  ${cssVars.join('\n  ')}\n}`;
}

// Get current theme from localStorage or default
export function getCurrentTheme(): string {
  if (typeof window !== 'undefined') {
    return localStorage.getItem('theme') || 'default';
  }
  return 'default';
}

// Set theme in localStorage
export function setCurrentTheme(themeName: string): void {
  if (typeof window !== 'undefined') {
    localStorage.setItem('theme', themeName);
  }
} 